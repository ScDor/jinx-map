export function normalizeZoneName(raw: string): string {
  return raw.replace(/^\uFEFF/, '').trim().normalize('NFKC');
}

export function mapCsvZoneToPolygonName(raw: string): string {
  const normalized = normalizeZoneName(raw);
  return CSV_TO_POLYGON_NAME_MAP[normalized] ?? normalized;
}

